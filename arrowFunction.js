//Arrow function =>
let add = (num1 , num2) => num1 + num2;
let sub = (num1 , num2) => num1 - num2;
let mul = (num1 , num2) => num1 * num2;
let mod = (num1 , num2) => num1 % num2;

console.log(add(5,6));
console.log(sub(8,6));
console.log(mul(10,6));
console.log(mod(17,6));

